#ifndef THREADPOOL_H
#define THREADPOOL_H

#include <vector>

namespace threadpool {

class thread;

class threadpool
{
	struct thread_home
	{
		thread* t;
		mailbox;
	};

	std::vector<thread*> threads;

	void join(thread& t) {
		threads.push_back(&t);
	}
};

}

#endif // THREADPOOL_H
